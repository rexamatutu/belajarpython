# kalau mau cek tipe data nya (type(x)) cntoh seperti di baris 23-27

# ini tipe data string
nama = "Udin"

# ini tipe data integer atau di singkat int
umur = 15

# ini tipe data float
nilai_matematika = 7.5

# ini tipe data array
barang_bawaan = ["sepatu", "kaos", "celana"]

# ini tipe data boolean
jawaban = True

print("Halo nama saya ",nama)
print("Umur saya ",umur)
print("barang bawaan saya ",barang_bawaan)
print("jawaban saya ",jawaban)

print("tipe data nama adalah ",type(nama))
print("tipe data umur adalah ",type(umur))
print("tipe data nilai matematika adalah ",type(nilai_matematika))
print("tipe data barang bawaan adalah ",type(barang_bawaan))
print("tipe data jawaban adalah ",type(jawaban))

